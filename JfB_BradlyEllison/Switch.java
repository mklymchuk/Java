public class Switch {
    public static void main(String[] args) {
        String course = "Math";

        switch(course) {
            case "Sciense":
            break;
            case "Geology":
            break;
            case "Math":
            break;
            case "Gymnastic":
            break;
            default:
            System.out.println("No course");
        }

        int fruit = 5;

        switch(fruit) {
            case 1:
            System.out.println("Banana");
            break;
            case 2:
            System.out.println("Kiwi");
            break;
            case 3:
            System.out.println("Apple");
            break;
            case 4:
            System.out.println("Orange");
            break;
            case 5:
            System.out.println("Pomegrante");
        }
    }
}
